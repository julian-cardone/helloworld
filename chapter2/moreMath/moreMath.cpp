#include "moreMath.h"
#include <cstdlib>
#include <iostream>

/**
 * This project is a library of simple arithmetic.
 */

/**
 * Add two numbers.
 */
int add(int x, int y)
{
    return x + y;
}

/**
 * Subtract two numbers.
 */
int subtract(int x, int y)
{
    return x - y;
}

/**
 * Multiply two numbers.
 */
int multiply(int x, int y)
{
    return x * y;
}

/**
 * Divide two numbers.
 */
int divide(int x, int y)
{
    return x / y;
}
